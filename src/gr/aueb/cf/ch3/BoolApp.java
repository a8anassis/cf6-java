/**
 * Δήλωση και αρχικοποίηση μεταβλητών
 * τύπου boolean.
 *
 * @author a8ana
 */
public class BoolApp {

    public static void main(String[] args) {
        boolean b1 = false;
        boolean b2 = true;
        boolean b3 = false;

        System.out.println("b1 = " + b1);
        System.out.println("b2 = " + b2);
        System.out.println("b3 = " + b3);
    }
}
